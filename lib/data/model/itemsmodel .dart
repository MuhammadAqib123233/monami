class ItemsModel {
  String? itemsId;
  String ?itemsName;
  String ?itemsNameAr;
  String ?itemsDesc;
  String ?itemsDescAr;
  String ?itemsImage;
  String ?itemslogo;
  String? itemsCount;
  String ? itemsActive;
  String? itemsPrice;
  String? itemsDiscount;
  String ?itemsDate;
  String? itemsCat;
  String ? categoryId;
  String? categoryName;
  String ?categoryNameAr;
  String? categoryImage;
  String ?categoryDaetime;
  String ?favorie;

  ItemsModel(
      {this.itemsId,
      this.itemsName,
      this.itemsNameAr,
      this.itemsDesc,
      this.itemsDescAr,
      this.itemsImage,
      this.itemslogo,
      this.itemsCount,
      this.itemsActive,
      this.itemsPrice,
      this.itemsDiscount,
      this.itemsDate,
      this.itemsCat,
      this.categoryId,
      this.categoryName,
      this.categoryNameAr,
      this.categoryImage,
      this.categoryDaetime,
      this.favorie,
      });

  ItemsModel.fromJson(Map<String, dynamic> json) {
    itemsId = json['items_id'];
    itemsName = json['items_name'];
    itemsNameAr = json['items_name_ar'];
    itemsDesc = json['items_desc'];
    itemsDescAr = json['items_desc_ar'];
    itemsImage = json['items_image'];
    itemslogo = json['items_logo'];
    itemsCount = json['items_count'];
    itemsActive = json['items_active'];
    itemsPrice = json['items_price'];
    itemsDiscount = json['items_discount'];
    itemsDate = json['items_date'];
    itemsCat = json['items_cat'];
    categoryId = json['category_id'];
    categoryName = json['category_name'];
    categoryNameAr = json['category_name_ar'];
    categoryImage = json['category_image'];
    favorie = json['favorie'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['items_id'] = this.itemsId;
    data['items_name'] = this.itemsName;
    data['items_name_ar'] = this.itemsNameAr;
    data['items_desc'] = this.itemsDesc;
    data['items_desc_ar'] = this.itemsDescAr;
    data['items_image'] = this.itemsImage;
    data['items_logo'] = this.itemslogo;
    data['items_count'] = this.itemsCount;
    data['items_active'] = this.itemsActive;
    data['items_price'] = this.itemsPrice;
    data['items_discount'] = this.itemsDiscount;
    data['items_date'] = this.itemsDate;
    data['items_cat'] = this.itemsCat;
    data['category_id'] = this.categoryId;
    data['category_name'] = this.categoryName;
    data['category_name_ar'] = this.categoryNameAr;
    data['category_image'] = this.categoryImage;
    data['category_daetime'] = this.categoryDaetime;
    data['category_daetime'] = this.categoryDaetime;
    return data;
  }
}
